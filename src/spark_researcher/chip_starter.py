            "    ])",
            "    return {",
            '        "returncode": 0, "stdout": stdout, "stderr": "",',
            f'        "metrics": {{"{metric_name}": metric_value, "verdict_confidence": verdict_confidence}},',
            '        "result": {"claim": "Placeholder.", "verdict": "supports" if focused else "inconclusive", "mechanism": "Placeholder."},',
