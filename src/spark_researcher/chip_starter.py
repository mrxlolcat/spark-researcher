def _slug(value: str) -> str:
    cleaned = re.sub(r"[^a-z0-9._-]+", "-", value.strip().lower())
    return cleaned.strip("-") or "chip"


def _package_name(chip_name: str) -> str:
    return _slug(chip_name).replace(".", "_").replace("-", "_")


def normalize_chip_name(domain: str, chip_name: str | None = None) -> str:
    candidate = _slug(chip_name or "") or _slug(domain)
    if candidate == "chip":
        candidate = _slug(domain)
    if not candidate.startswith("domain-chip-"):
        candidate = f"domain-chip-{candidate}"
    return candidate


def _desktop_root() -> Path:
    desktop = Path.home() / "Desktop"
    return desktop if desktop.exists() else Path.home()


def _spark_repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def _next_steps(chip_root: Path) -> list[str]:
    return [
        f"cd {chip_root}",
        "git init",
        "git branch -m main",
        "python -m pip install -e .",
        f"python -m pip install -e {_spark_repo_root()}",
        f"python -m spark_researcher.cli chips validate --config {chip_root / 'spark-researcher.project.json'}",
    ]


def resolve_chip_target(target_dir: Path | None, chip_name: str) -> Path:
    if target_dir is None:
        return (_desktop_root() / chip_name).resolve()
    candidate = target_dir.expanduser()
    if candidate.is_absolute():
        return candidate.resolve()
    return (_desktop_root() / candidate).resolve()


def ensure_external_chip_target(target_dir: Path) -> Path:
    resolved = target_dir.resolve()
    repo_root = _spark_repo_root()
    if resolved == repo_root or resolved.is_relative_to(repo_root):
        raise ValueError(
            f"Chip targets must live outside spark-researcher. Refusing to create chip inside `{repo_root}`."
        )
    return resolved


def _write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.rstrip() + "\n", encoding="utf-8")


